CREATE TABLE `pina` (
`id` int(4) UNSIGNED NOT NULL AUTO_INCREMENT COMMENT '主键',
`11` varchar(64) NOT NULL COMMENT '11',
`222` varchar(128) NULL COMMENT '22',
`vcodes` varchar(256) NOT NULL DEFAULT '' COMMENT '333',
`status` smallint(2) NOT NULL DEFAULT 1 COMMENT '状态 1-有效 0-无效',
`description` varchar(128) NULL COMMENT '描述',
`created_date` TIMESTAMP NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
`modified_date` TIMESTAMP NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '修改时间',
PRIMARY KEY (`id`)
) ENGINE=InnoDB AUTO_INCREMENT=1 DEFAULT CHARSET=utf8 COLLATE=utf8_bin COMMENT='业务表';

